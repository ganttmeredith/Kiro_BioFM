"""Chat router: streaming responses via Amazon Bedrock."""

import json
import os

import boto3
from botocore.exceptions import BotoCoreError, NoCredentialsError

from fastapi import APIRouter
from fastapi.responses import JSONResponse, StreamingResponse

from backend.models import ChatRequest
from backend.services.chat_service import ChatService
from backend.services.data_service import data_service

router = APIRouter()

_NOT_LOADED = "Dataset not loaded. Load data first to enable context-aware chat."
_BEDROCK_UNAVAILABLE = (
    "Bedrock client could not be initialised. Check AWS credentials and region."
)

