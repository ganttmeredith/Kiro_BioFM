@router.post("/stream")
async def chat_stream(request: ChatRequest):
    """POST /api/chat/stream — stream tokens from Bedrock via SSE.

    - HTTP 409: dataset not loaded
    - HTTP 503: Bedrock client cannot be initialised
    """
    if not data_service.get_status().loaded:
        return JSONResponse({"detail": _NOT_LOADED}, status_code=409)

    try:
        boto3.client("bedrock-runtime", region_name=os.environ.get("AWS_DEFAULT_REGION", "us-west-2"))
    except (BotoCoreError, NoCredentialsError) as exc:
        return JSONResponse(
            {"detail": f"{_BEDROCK_UNAVAILABLE} ({exc})"},
            status_code=503,
        )

    service = ChatService()
    return StreamingResponse(
        service.stream_response(request.messages, request.app_context),
        media_type="text/event-stream",
    )


class _ReportRequest(ChatRequest):
    chart_messages: list = []


@router.post("/report")
async def generate_report(request: _ReportRequest):
    """POST /api/chat/report — stream a scientific report from the conversation as SSE."""
    if not data_service.get_status().loaded:
        return JSONResponse({"detail": _NOT_LOADED}, status_code=409)

    model_id = os.environ.get("BEDROCK_MODEL_ID", "us.anthropic.claude-sonnet-4-6")
    region = os.environ.get("AWS_DEFAULT_REGION", "us-west-2")

    try:
        client = boto3.client("bedrock-runtime", region_name=region)
    except (BotoCoreError, NoCredentialsError) as exc:
        return JSONResponse({"detail": f"{_BEDROCK_UNAVAILABLE} ({exc})"}, status_code=503)

    # Use chart_messages if provided (includes vision content), otherwise build from transcript
    if request.chart_messages:
        messages = request.chart_messages
    else:
        transcript_lines = []
        for msg in request.messages:
            role = "Researcher" if msg.role == "user" else "Assistant"
            transcript_lines.append(f"**{role}:** {msg.content}")
        transcript = "\n\n".join(transcript_lines)
        messages = [{"role": "user", "content": (
            "Based on the following conversation, generate a formal scientific report:\n\n"
            f"---\n{transcript}\n---"
        )}]

    def stream_report():
        try:
            response = client.invoke_model_with_response_stream(
                modelId=model_id,
                body=json.dumps({
                    "anthropic_version": "bedrock-2023-05-31",
                    "max_tokens": 4096,
                    "system": _REPORT_SYSTEM_PROMPT,
                    "messages": messages,
                }),
                contentType="application/json",
                accept="application/json",
            )
            for event in response["body"]:
                chunk = json.loads(event["chunk"]["bytes"])
                if chunk.get("type") == "content_block_delta":
                    delta = chunk.get("delta", {}).get("text", "")
                    if delta:
                        yield f"data: {json.dumps({'delta': delta})}\n\n"
            yield f"data: {json.dumps({'done': True})}\n\n"
        except Exception as exc:  # noqa: BLE001
            yield f"data: {json.dumps({'error': str(exc)})}\n\n"

    return StreamingResponse(stream_report(), media_type="text/event-stream")
